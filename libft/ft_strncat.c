#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*as1;
	size_t	i;
	size_t	j;

	as1 = s1;
	i = ft_strlen(s1);
	j = 0;
	while ((s2[j] != '\0') && (j < n))
		as1[i++] = s2[j++];
	as1[i] = '\0';
	return (as1);
}
