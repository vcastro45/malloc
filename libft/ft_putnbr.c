#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			write(1, "-", 1);
		}
		if (n >= 10)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
}
